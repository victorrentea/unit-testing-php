namespace PhpUnitWorkshopTest\mocks;


use PHPUnit\Framework\TestCase;


function captureArg(&$arg, TelemetryDiagnosticControlsTest $instance) {
    return $instance->callback( function($argToMock ) use ( &$arg ) {
        $arg = $argToMock;
        return true;
    } );
}
class TelemetryDiagnosticControlsLenesTest extends TestCase
{

    private $clientMock;
    private $controls;

    protected function setUp(): void
    {
        parent::setUp(); // TODO: Change the autogenerated stub
        // PREGATIRE
        $this->clientMock = $this->createMock(TelemetryClient::class);
        $this->controls = new TelemetryDiagnosticControls($this->clientMock);
    }

    /** @test */
    public function throwsWhenNotOnline() { // rosie
        $this->expectExceptionMessage("connect");
        $this->clientMock->method("getOnlineStatus")->willReturn(false); // method stubbing

        $this->controls->checkTransmission();
    }

    /** @test */
    public function verde() {
        $this->clientMock->expects($this->once())->method("disconnect");
        $this->clientMock->method("getOnlineStatus")->willReturn(true);
        $this->clientMock->expects($this->once())->method("send")->with("AT#UD");
        $this->clientMock->/*expects($this->once())->*/method("receive")->willReturn("mamaie");

        $this->controls->checkTransmission();

        self::assertEquals("mamaie", $this->controls->getDiagnosticInfo());
    }


    /** @test */
    public function configuresClient() {
        $config = null;
        $this->clientMock->method("configure")->with(captureArg($config, $this));


        $this->controls->checkTransmission();
        self::assertEquals(TelemetryClientConfiguration::ACK_NORMAL, $config->getAckMode());
    }


    /** @test */
    public function createConfig() {
        $config = $this->controls->createConfig();
        self::assertEquals(TelemetryClientConfiguration::ACK_NORMAL, $config->getAckMode());
        self::assertLessThan(60*1000, time() - $config->getSessionStart());
        self::assertNotNull( $config->getSessionStart());
        self::assertNotNull( time());
    }




}

